const int INF = 1<<29;

using namespace std;

const int N = 9;

int minDistance(int dist[], bool sptSet[])
{
    int min = INT_MAX, min_index;
 
    for (int v = 0; v < N; v++)
        if (sptSet[v] == false && dist[v] <= min)
            min = dist[v], min_index = v;
 
    return min_index;
}
 
void printSolution(int dist[])
{
    cout <<"Vertex \t Distance from Source" << endl;
    for (int i = 0; i < N; i++)
        cout  << i << " \t\t"<<dist[i]<< endl;
}

void dijkstra(int graph[N][N], int source){

    int dist[N];

    bool spottedIndex[N];

    std::fill_n(dist, N, INF);
    std::fill_n(spottedIndex, N, false);

    dist[source] =  0;

    for(int i = 0; i < N-1; i ++){

        int u = minDistance(dist, spottedIndex);
 
        spottedIndex[u] = true;
 
        for (int v = 0; v < N; v++)
 
            if (!spottedIndex[v] && graph[u][v] && dist[u] != INT_MAX
                && dist[u] + graph[u][v] < dist[v])
                dist[v] = dist[u] + graph[u][v];
    }
    
    printSolution(dist);

}


